int main(void)
{

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();
  /* Configure the system clock */
  SystemClock_Config();
  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_CAN1_Init();
  MX_CAN2_Init();
  /* USER CODE BEGIN 2 */
  lcd_init(BIT_4_MODE);
  lcd_print(0, 0, "Status: Init...");
  lcd_print(0, 1, "Data:");
  HAL_Delay(200);

  HAL_GPIO_WritePin(GPIOC, STB1_Pin, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(GPIOC, STB2_Pin, GPIO_PIN_RESET);
  aData[0] = 'R';
  aData[1] = 'e';
  aData[2] = 'c';
  aData[3] = 'e';
  aData[4] = 'i';
  aData[5] = 'v';
  aData[6] = 'e';
  aData[7] = 'd';
  p1header.DLC = 8;
  p1header.IDE = CAN_ID_STD;
  p1header.StdId = 0x001;
  p1header.RTR = CAN_RTR_DATA;

  sFilterConfig.FilterFIFOAssignment = CAN_FILTER_FIFO0;
  sFilterConfig.FilterIdHigh = 0x65D;
  sFilterConfig.FilterIdLow = 0x651;
  sFilterConfig.FilterMaskIdHigh = 0;
  sFilterConfig.FilterMaskIdLow = 0;
  sFilterConfig.FilterScale = CAN_FILTERSCALE_32BIT;
  sFilterConfig.FilterActivation = ENABLE;
  HAL_CAN_ConfigFilter(&hcan1, &sFilterConfig);
  HAL_CAN_ConfigFilter(&hcan2, &sFilterConfig);

  HAL_CAN_Start(&hcan1);
  HAL_CAN_Start(&hcan2);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    lcd_print(7, 0, "Sending  ");
    HAL_CAN_AddTxMessage(&hcan1, &p1header, aData, &pTxMailbox);
    lcd_print(7, 0, "Receiving");
    HAL_CAN_GetRxMessage(&hcan2, CAN_RX_FIFO0, &p2header, received_data);
    lcd_print(5, 1, (char *)received_data);
  }
  /* USER CODE END 3 */
}

